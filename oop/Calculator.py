class Calculator:
    # x = 10

    def add(self, a, b):
        return a + b

    def sub(self, a, b):
        return a - b

    def div(self, a, b):
        return a / b

    def mul(self, a, b):
        return a * b

calc = Calculator()
print(calc.add(10, 10))
print(calc.sub(10,10))
print(calc.div(10, 10))
print(calc.mul(10, 10))

